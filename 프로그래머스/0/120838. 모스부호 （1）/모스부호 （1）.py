def solution(letter):
    morse = {
        '.-': 'a', '-...': 'b', '-.-.': 'c', '-..': 'd', '.': 'e', '..-.': 'f',
        '--.': 'g', '....': 'h', '..': 'i', '.---': 'j', '-.-': 'k', '.-..': 'l',
        '--': 'm', '-.': 'n', '---': 'o', '.--.': 'p', '--.-': 'q', '.-.': 'r',
        '...': 's', '-': 't', '..-': 'u', '...-': 'v', '.--': 'w', '-..-': 'x',
        '-.--': 'y', '--..': 'z'
    }
    # 모스부호를 공백으로 분리하여 리스트로 변환
    morse_list = letter.split(' ')
    # 각 모스부호를 알파벳으로 변환하여 리스트 생성
    decoded_chars = [morse[code] for code in morse_list]
    # 리스트를 문자열로 결합하여 반환
    return ''.join(decoded_chars)
